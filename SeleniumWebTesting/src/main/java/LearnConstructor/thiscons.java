package LearnConstructor;
//learn this keyword
public class thiscons {
	int rollnumber;
	String name;
	float fees;
	public thiscons(int rollnumber,String name,float fees) {
		this.rollnumber=rollnumber;
		this.name=name;
		this.fees=fees;
	   }
	public void record() {
		System.out.println(rollnumber+name+fees);
	}
	
	
	
	}




